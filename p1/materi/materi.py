# menggunakan operator % untuk format string
nama = "Zakki"
umur = 19
tinggi = 1.8

print("Nama saya adalah %s, umur saya %d tahun, dan tinggi saya %.2f meter."%(nama,umur,tinggi))

# %s placeholder untuk string(nama)
# %d placeholder untuk integer(umur)
# %.2f placeholder untuk flotat dengan 2 angka dibelkang desimal(tinggi)