def luas_persegi_panjang(panjang, lebar):
    luas = panjang * lebar
    return luas

print(f"Luas persegi panjang dengan panjang 6cm dan lebar 10cm adalah: {luas_persegi_panjang(6, 10)} cm²")
print(f"Luas persegi panjang dengan panjang 10cm dan lebar 20cm adalah: {luas_persegi_panjang(10, 20)} cm²")
print(f"Luas persegi panjang dengan panjang 15cm dan lebar 30cm adalah: {luas_persegi_panjang(15, 30)} cm²")

